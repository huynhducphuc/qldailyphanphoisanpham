    private void showDataToTableAndCheckbox(ArrayList<Supermarket> list, JTable tbl) {
        Vector vCol = new Vector();
        Vector vRow = new Vector();
        vCol.add("id");
        vCol.add("District");
        vCol.add("Name");
        vCol.add("Address");
        vCol.add("Phone Number");
        vCol.add("");
        ArrayList<District> listDistricts = new DistrictService().getDistricts();
        for (int i = 0; i < list.size(); i++) {
            Vector temp = new Vector();
            String districtName = "";
            temp.add(list.get(i).getId());
            for(int j=0;j<listDistricts.size();j++){
                if(listDistricts.get(j).getDistrictId() == list.get(i).getDistrictId())
                    districtName = listDistricts.get(j).getDistrictName();
            }
            temp.add(districtName);
            temp.add(list.get(i).getName());
            temp.add(list.get(i).getAddress());
            temp.add(list.get(i).getPhone());
            vRow.add(temp);
        }

        tbl.setModel(new DefaultTableModel(vRow, vCol));
        TableColumn cbacc = tbl.getColumnModel().getColumn(checkBox);
        cbacc.setCellEditor(tbl.getDefaultEditor(Boolean.class));
        cbacc.setCellRenderer(tbl.getDefaultRenderer(Boolean.class));
        cbacc.setMaxWidth(30);
    }
