    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        Vector v = null;
        v = CheckTable.getValueTable(jTableUser, checkBox);

        if (v.size() == 0) {
            JOptionPane.showMessageDialog(this, "You must select at least one.");
            return;
        } else {
            int choice = JOptionPane.showConfirmDialog(this, "Are you sure delete ?", "", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                String[] del_ID = null;
                String abc = "";
                for (int i = 0; i < v.size(); i++) {
                    abc += String.valueOf(jTableUser.getValueAt(Integer.parseInt(v.get(i).toString()), 0) + ",");
                }
                del_ID = abc.substring(0, abc.length() - 1).split(",");
                int del = 0;
                try {
                    for (int i = 0; i < del_ID.length; i++) {
                        del = supermarketService.deleteSupermarket(Integer.parseInt(del_ID[i]));
                    }
                    if (del > 0) {
                        JOptionPane.showMessageDialog(this, "Delete succsefull.");
                        initDataTable();
                    }else {
                        JOptionPane.showMessageDialog(this, "Cannot Delete.");
                        return;
                    }
                } catch (Exception ex) {
                    Logger.getLogger(Manage_Supermarket.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                return;
            }
        }

    }//GEN-LAST:event_btndeleteActionPerformed

